function generateData(sliderContent, navContent, productContent, fixAdContent) {
	var $wrap = $('#wrap');
	var $sliderWrap = $('#sliderWrap');
	var $dropList = $('#dropList');
	var $navList = $('#navList');
	var $subWrap = $('#subWrap');
	var $product = $('#product');

	var dataFixAd = '';
	var dataSlider = '';
	var dataNav = '';
	var dataDrop = '';
	var dataSub = '';
	var dataProduct ='';

	for (var n = 0; n < sliderContent.length; n++) {
		dataSlider += '<a href="' + sliderContent[n].link + '" target="_blank"><img src="' + sliderContent[n].banner +
		'" alt="優惠活動"></a>';
	}

	for (var i = 0; i < navContent.length; i++) {
		dataNav += '<li class="list__item nav__item"><span>' + navContent[i][0] + '</span></li>';
		dataDrop += '<li class="list__item drop__item"><span>' + navContent[i][0] + '</span></li>';

		dataSub += '<ul class="sub__list">';

		for (var j = 0; j < navContent[i][1].length; j++) {
			dataSub += '<li class="sub__item"><span>' + navContent[i][1][j] + '</span></li>';
		}

		dataSub += '</ul>';
	}

	for (var k = 0; k < productContent.length; k++) {
		dataProduct += '<div class="product__wrap"><ul class="product__list">';

		for (var m = 0; m < productContent[k].length; m++) {
			dataProduct += '<li class="product__item"><a href="product.html" title="'+ productContent[k][m].name + 
				'"><div class="product__img ' + (productContent[k][m].video?"video":"") + '"><img src="' +
				productContent[k][m].productImg + '" alt="' + productContent[k][m].name + '"></div><p class="product__name">' +
				productContent[k][m].name + '</p><div class="product__price"><span>$</span><span>' + productContent[k][m].price +
				'</span></div></a></li>';
		}
		dataProduct += '</ul></div>';
	}

	dataFixAd = '<div id="fixAd" class="fix-ad"><a href="' + fixAdContent.link + '" target="_blank"><img src="' + fixAdContent.adImg +
		'" alt="優惠活動"></a></div>';

	if(sliderContent) {$sliderWrap.append(dataSlider);}
	if(navContent) {$dropList.append(dataDrop);}
	if(navContent) {$navList.append(dataNav);}
	if(navContent) {$subWrap.append(dataSub);}
	if(productContent) {$product.append(dataProduct);}
	if(fixAdContent) {$wrap.append(dataFixAd);}
}

